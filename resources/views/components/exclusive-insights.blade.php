<script>
    // Function to update crypto prices (simulated real-time updates)
    function refreshPrices() {
        const updateTime = new Date().toLocaleTimeString();
        document.getElementById('update-time').innerHTML = updateTime;
        
        // Add loading effect
        const prices = document.querySelectorAll('.font-bold.text-navy-900');
        prices.forEach(price => {
            price.style.opacity = '0.5';
            setTimeout(() => {
                price.style.opacity = '1';
            }, 300);
        });
    }
    
    // Auto-refresh every 30 seconds
    setInterval(refreshPrices, 30000);
    
    // Set initial update time
    document.addEventListener('DOMContentLoaded', function() {
        const now = new Date().toLocaleTimeString();
        document.getElementById('update-time').innerHTML = now;
    });
</script>

